/**
 * Lấy câu hỏi ngẫu nhiên từ danh sách
 * @param {Array} usedQuestions - Mảng các index câu hỏi đã sử dụng
 * @returns {Object|null} - Object chứa câu hỏi và index
 */
export const getRandomKnowledgeQuestion = (usedQuestions = []) => {
  const availableQuestions = knowledgeQuestions.filter((_, index) => 
    !usedQuestions.includes(index)
  );
  
  if (availableQuestions.length === 0) {
    return null;
  }
  
  const randomIndex = Math.floor(Math.random() * availableQuestions.length);
  const question = availableQuestions[randomIndex];
  const originalIndex = knowledgeQuestions.indexOf(question);
  
  return {
    question,
    index: originalIndex
  };
};

